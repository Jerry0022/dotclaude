#!/usr/bin/env node
/**
 * @script gen-dk-index
 * @version 0.1.0
 * @plugin dotclaude-dev-ops
 * @description Generates deep-knowledge/INDEX.md from all .md files in the directory.
 *   Reads the first heading and first description line from each file.
 *   Called automatically by ship_build; can also be run standalone.
 *
 *   Usage: node scripts/gen-dk-index.js [deep-knowledge-dir]
 */

import { readdirSync, readFileSync, writeFileSync } from "node:fs";
import { join, resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const DEFAULT_DIR = resolve(__dirname, "..", "deep-knowledge");

function extractMeta(filePath) {
  const content = readFileSync(filePath, "utf8");
  const lines = content.split("\n");

  // First heading (# Title)
  const heading = lines.find((l) => /^#\s+/.test(l));
  const title = heading ? heading.replace(/^#+\s+/, "").trim() : null;

  // First non-empty, non-heading line = description
  const desc = lines.find(
    (l) => l.trim() && !/^#/.test(l) && !/^```/.test(l),
  );

  const clean = desc ? desc.trim() : "";
  const capped = clean.length > 80 ? clean.slice(0, 77) + "..." : clean;
  return { title, desc: capped };
}

function generate(dir) {
  const dkDir = resolve(dir);
  const files = readdirSync(dkDir)
    .filter((f) => f.endsWith(".md") && f !== "INDEX.md")
    .sort();

  if (!files.length) {
    console.error("[gen-dk-index] No .md files found in", dkDir);
    process.exit(1);
  }

  const rows = files.map((f) => {
    const { title, desc } = extractMeta(join(dkDir, f));
    return `| [${f}](${f}) | ${title || f} | ${desc} |`;
  });

  const index = [
    "<!-- AUTO-GENERATED by scripts/gen-dk-index.js — do not edit manually -->",
    "# Deep Knowledge Index",
    "",
    "Quick-reference for all deep-knowledge topics. Read this FIRST to find the right file.",
    "",
    "| File | Topic | Summary |",
    "|------|-------|---------|",
    ...rows,
    "",
  ].join("\n");

  const outPath = join(dkDir, "INDEX.md");
  const existing = (() => {
    try {
      return readFileSync(outPath, "utf8");
    } catch {
      return null;
    }
  })();

  if (existing === index) {
    console.error("[gen-dk-index] INDEX.md is up-to-date");
    return false;
  }

  writeFileSync(outPath, index, "utf8");
  console.error(
    `[gen-dk-index] INDEX.md generated (${files.length} entries)`,
  );
  return true;
}

generate(process.argv[2] || DEFAULT_DIR);
